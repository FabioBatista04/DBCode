CREATE DATABASE IF NOT EXISTS DBCode;
USE DBCode;

CREATE TABLE IF NOT EXISTS `USER` (
 ID BIGINT PRIMARY KEY AUTO_INCREMENT,
 EMAIL VARCHAR(255) NOT NULL UNIQUE,
 USERNAME VARCHAR(255) NOT NULL UNIQUE,
 PASSWORD VARCHAR(255) NOT NULL,
 PROFILE VARCHAR(255) NOT NULL,
 RESET_CODE VARCHAR(12),
INDEX IDX_EMAIL(EMAIL),
INDEX IDX_USERNAME(USERNAME)
);

CREATE TABLE IF NOT EXISTS `REQUIREMENT` (
ID BIGINT PRIMARY KEY AUTO_INCREMENT,
`NAME` VARCHAR(255) NOT NULL UNIQUE,
QUALIFICATION VARCHAR(255) NOT NULL UNIQUE,
DESCRIPTION VARCHAR(255) NOT NULL,
FILE_ESPECIFICATION VARCHAR(20),
FILE_LAYOUT VARCHAR(255) NOT NULL,
INDEX IDX_NAME(`NAME`)
);

CREATE TABLE IF NOT EXISTS PLANO (
id BIGINT PRIMARY KEY AUTO_INCREMENT,
nome VARCHAR(255) NOT NULL UNIQUE,
data_inclusao VARCHAR(255) NOT NULL UNIQUE,
responsavel_inclusao VARCHAR(255) NOT NULL UNIQUE
);